
public class Calculator {
	
	public double mult (double a, double b)
	{
		double c = a *b;
		return c;
	}
	public double div (double a, double b)
	{
		double c = a/b;
		return c;
	}
	public double add (double a, double b)
	{
		double c = a+b;
		return c;
	}
	public double sub (double a, double b)
	{
		double c = a-b;
		return c;
	}
	public double mod (double a, double b)
	{
		double c = a%b;
		return c;
	}
}
